    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (!$request->ajax()) return redirect('/');
        $orden = new Orden();
        $orden->numero_orden = $request->numero_orden;
        $orden->proveedor_idproveedor = $request->proveedor_idproveedor;
        $orden->fechasolicitud = $request->fechasolicitud;
        $orden->fechaentrega = $request->fechaentrega;
        $orden->tipocompra = $request->tipocompra;
        $orden->detalle = $request->detalle;
        $orden->save();
    }

