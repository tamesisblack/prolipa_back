    public function store(Request $request)
    {
        if (!$request->ajax()) return redirect('/');
        $proveedor = new Proveedor();
        $proveedor->nombre_proveedor = $request->nombre_proveedor;
        $proveedor->nombre_contacto = $request->nombre_contacto;
